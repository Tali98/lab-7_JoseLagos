    private void cb_listarItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cb_listarItemStateChanged
        name = (String) cb_listar.getSelectedItem();
        Pieza p = new Pieza();
        int a = listaCarros.size();
        for (a = listaCarros.size() - 1; a >= 0; a--) {
            if (listaCarros.get(a).getNombre().equals(name)) {
                p = listaCarros.get(a);
            }
        }

        DefaultMutableTreeNode raiz = new DefaultMutableTreeNode(p);
        DefaultTreeModel model = new DefaultTreeModel(raiz);
        jt_listar.setModel(model);
        String nombre = null;
        String material = null;
        int num = 0;
        Pieza padre = null;
        if (p.getListPiezasinternas().size() > 0) {
            nombre = null;
            material = null;
            num = 0;
            padre = null;
            Print(p, (DefaultMutableTreeNode) raiz.getRoot());
        }


    }//GEN-LAST:event_cb_listarItemStateChanged

