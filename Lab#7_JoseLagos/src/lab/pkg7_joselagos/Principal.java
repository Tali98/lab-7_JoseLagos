    private void jm_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jm_eliminarActionPerformed
        Object v1 = jt_listar.getSelectionPath().getLastPathComponent();
        nodo_el = (DefaultMutableTreeNode)v1;
        DefaultTreeModel m = (DefaultTreeModel) jt_listar.getModel();
        m.removeNodeFromParent(nodo_el);
    }//GEN-LAST:event_jm_eliminarActionPerformed
    public void Print(Pieza p, DefaultMutableTreeNode nodo) {
        try {
            ArrayList<Pieza> lista = new ArrayList();
            for (Pieza f : p.getListPiezasinternas()) {
                lista.add(f);
            }
            for (Pieza f : lista) {
                if (f.getListPiezasinternas().size() > 0) {
                    DefaultMutableTreeNode n = new DefaultMutableTreeNode(f.getNombre());
                    nodo.add(n);
                    Print(f, n);
                } else {
                    DefaultMutableTreeNode n = new DefaultMutableTreeNode(f.getNombre());
                    nodo.add(n);
                }
            }
        } catch (Exception e) {
        }

    }

